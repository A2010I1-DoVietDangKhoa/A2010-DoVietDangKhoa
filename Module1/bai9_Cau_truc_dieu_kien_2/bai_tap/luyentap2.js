function bai7() {
    let a = document.getElementById("soa2").value;
    let b = document.getElementById("sob2").value;
    let c = document.getElementById("soc2").value;
    let n = (b < 0)? b : "+ " + b;
    let m = (c < 0)? c : "+ " + c;
    document.getElementById("result71").innerHTML = a + "x^2 " + n + "x " + m + " = 0";
    let delta = Math.pow(parseInt(b), 2) - 4*parseInt(a)*parseInt(c);
    if (a==0){
        if (b == 0 ){
            if (c == 0) {
                document.getElementById("result72").innerHTML = "Phương trình vô số nghiệm.";
            }
            else {
                document.getElementById("result72").innerHTML = "Phương trình vô nghiệm.";
            }
        }
        else{
            let x = -c/b;
            document.getElementById("result72").innerHTML = "Phương trình có 1 nghiệm " + x;
        }
    }
    else{
        if(delta < 0){
            document.getElementById("result72").innerHTML = "Phương trình vô nghiệm.";
        }
        else if(delta == 0){
            let x1 = -b/(2*a);
            document.getElementById("result72").innerHTML = "Phương trình có nghiệm kép " + x1;
        }
        else{
            let x2 = (-b + Math.sqrt(delta))/(2*a);
            let x3 = (-b - Math.sqrt(delta))/(2*a);
            document.getElementById("result72").innerHTML = "Phương trình có 2 nghiệm " + x2 + " và " +x3;
        }
    }
}

